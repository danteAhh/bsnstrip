package com.bsnstrip.pojo;

public class Country {
	private String City;
	private String id;
	private String name;
	public String getCity() {
		return City;
	}
	public void setCity(String city) {
		City = city;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
